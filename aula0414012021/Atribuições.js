//Atribuições de valores de variáveis
//Uso de variáveis para a atribuições

//Adição
let um = 5;
let dois =7;
console.log("Resultado:", um + dois);

//Subtração
let tres = 8;
let quatro = 9;
console.log("Resultado:", tres - quatro);

//Multiplicação
let cinco = 10;
let seis = 15;
console.log("Resultado;", cinco * seis);

//Divisão
let sete = 10;
let oito = 5;
console.log("Resultado:", sete / oito);

//Ordem de precedência do operador no JS
let dez = 10;
let onze = 11;
let treze = 13;
console.log("Resultado:", dez * onze + treze);
console.log("Resultado:", dez / onze - treze);

//Ordem de precedência do agrupador no JS
let quatorze = 14;
let quinze = 15;
let dezesseis = 16;
console.log("Resultado:", quatorze + (quinze * dezesseis));

//Operador de concatenação 
let  a = "Programador";
